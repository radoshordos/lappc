<p>Pokud se vada projeví v prvních 6 měsících od převzetí plnění, považuje se za vadu, která existovala již v době převzetí věci, pokud se neprokáže opak či to neodporuje povaze věci. V případě, že věc při převzetí kupujícím - spotřebitelem není ve shodě s kupní smlouvou (tzv. rozpor s kupní smlouvou), má kupující - spotřebitel právo na to, aby prodávající bezplatně a bez zbytečného odkladu věc uvedl do stavu odpovídajícího kupní smlouvě, a to podle požadavku kupujícího buď výměnou věci, nebo její opravou; není-li takový postup možný, může kupující požadovat přiměřenou slevu z ceny věci nebo od smlouvy odstoupit. To neplatí, pokud kupující před převzetím věci o rozporu s kupní smlouvou věděl nebo rozpor s kupní smlouvou sám způsobil. Rozpor s kupní smlouvou, který se projeví během šesti měsíců ode dne převzetí věci, se považuje za rozpor existující již při jejím převzetí, pokud to neodporuje povaze věci nebo pokud se neprokáže opak. V případě rozporu zboží se smlouvou má spotřebitel nárok na tzv. zjednání nápravy a to:</p>

<ul>
    <li>bezplatným uvedením spotřebního zboží do stavu odpovídajícího smlouvě prostřednictvím opravy,</li>
    <li>přiměřeným snížením kupní ceny,</li>
    <li>náhradním dodáním zboží,</li>
    <li>odstoupením od smlouvy</li>
</ul>

<p>Nejprve může kupující - spotřebitel požadovat u prodávajícího bezplatnou opravu nebo bezplatné náhradní dodání zboží. Pokud volbu kupujícího - spotřebitele nebude možné splnit, může požadovat přiměřenou slevu nebo od smlouvy odstoupit. 
    Reklamace (uplatnění odpovědnosti za vady) bude vyřízena bez zbytečného odkladu, nejpozději do 30ti kalendářních dnů ode dne doručení reklamace, pokud se prodávající s kupujícím nedohodne jinak. Po uplynutí této lhůty má spotřebitel stejná práva, jako by se jednalo o vadu, kterou nelze odstranit. Práva z odpovědnosti za vady se uplatňují u prodávajícího, u kterého byla věc zakoupena. Je-li však v záručním listě uveden jiný subjekt určený k opravě, který je v místě prodávajícího nebo v místě pro kupujícího bližším, může kupující uplatnit právo na opravu u subjektu určeného k provedení záruční opravy. V případě, že není možné zajistit opravu tímto způsobem, zajistí opravu prodávající.</p>

